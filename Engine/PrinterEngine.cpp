void CPrinterEngine::PrintLogo()
{
	uchar	*psLogoData;
	int		iWidth, iHeigh;

	psLogoData = (unsigned char*)NULL;
	//GetNowPrnLogo(&psLogoData);
	psLogoData = CPrinterEngine::ms_pPrnLogo;
	if (psLogoData!=NULL) {
		iWidth = 0;
		iHeigh = 0;
		GetLogoWidthHeigh(psLogoData, &iWidth, &iHeigh);
		if (iWidth<384) {
			iWidth = (384-iWidth)/2;	// let logo be printed at center
		}
		else {
			iWidth = 0;
		}
		PrnLeftIndent(0);
		PrnSetNormal();
		PrnLogo(psLogoData);
		PrnLeftIndent(0);
		PrnStr("\n");
	}
}

